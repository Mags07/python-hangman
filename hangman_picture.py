def get_hangman_picture(remaining_attempts):
    max_attempts = 6
    stages = ["""
        ------
        |    |
        |
        |
        |
        |
        |
    ------------
    """, """
        ------
        |    |
        |    O
        |
        |
        |
        |
    ------------
    """, """
        ------
        |    |
        |    O
        |    |
        |    |
        |
        |
    ------------
    """, """
        ------
        |    |
        |    O
        |    |
        |    |
        |  ./
        |
    ------------
    """, """
        ------
        |    |
        |    O
        |    |
        |    |
        |  ./ \.
        |
    ------------
    """, """
        ------
        |    |
        |    O
        |  --|
        |    |
        |  ./ \.
        |
    ------------
    """, """
        ------
        |    |
        |    O
        |  --|--
        |    |
        |  ./ \.
        |
    ------------
    """]
    return stages[max_attempts - remaining_attempts]